class UserBase(BaseModel):
    # Use a plain string here because the default admin uses a .local domain
    # which is not considered a valid RFC-compliant email by EmailStr.
    username: Optional[str] = Field(None, max_length=100)
    email: str

class UserCreate(UserBase):
    password: str
    invitation_token: Optional[str] = None

