class TestBase(BaseModel):
    # detection_id can be null for scenario / telemetry-only runs,
    # so keep this optional to match the database shape.
    detection_id: Optional[str] = Field(None, max_length=50)
    technique_id: str = Field(..., max_length=50)
    marker: Optional[str] = Field(None, max_length=100) # Marker can be generated by backend
    environment: str = Field("lab", max_length=20)  # "lab" | "prod"
    endpoint: Optional[str] = Field(None, max_length=255)
    initiated_by_user_id: Optional[int] = None
    initiated_by_email: Optional[str] = Field(None, max_length=255)
    initiated_by_username: Optional[str] = Field(None, max_length=100)
    initiated_by_role: Optional[str] = Field(None, max_length=100)
    status: str = Field("pending", max_length=50)
    result: Optional[str] = Field(None, max_length=1000)
    score: Optional[int] = None
    # summary: Optional[str] = None # Removed as per new model
    started_at: Optional[datetime] = None
    finished_at: Optional[datetime] = None

